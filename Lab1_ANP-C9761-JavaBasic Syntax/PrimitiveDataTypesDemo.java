public class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        byte age = 25;
        short populationInThousand = 15000;
        int distanceInMeters = 100000;
        long starsInUniverse = 1000000000000L;
        float pi = 3.14f;
        double e = 2.7182818284;
        char initial = 'A';
        boolean isJavaFun = true;

        System.out.println("Byte: " + age);
        System.out.println("Short: " + populationInThousand);
        System.out.println("Int: " + distanceInMeters);
        System.out.println("Long: " + starsInUniverse);
        System.out.println("Float: " + pi);
        System.out.println("Double: " + e);
        System.out.println("Char: " + initial);
        System.out.println("Boolean: " + isJavaFun);
    }
}
